    public static void buildWindow(){
        String[] realWorld = new String[world.length];
        System.arraycopy(world, 0, realWorld, 0, world.length);
        for(Mob m: mobs){
            //System.out.println("Y: " + m.getY() + "         " + realWorld.length);
            //System.out.println("X: " + m.getX() + "         " + realWorld[Random.Int(20)].length());
            realWorld[m.getY()] = changeChar(realWorld[m.getY()], m.getX(), 'w');
        }
        int j = 0;
        for(int i = windowY; i < windowY + heroRealm.length; i++){
            if(j == 0) {
                heroRealm[j] = "```" + realWorld[i].substring(windowX, windowX + heroRealm[j].length() - 4) + "\n";
                j++;
                continue;
            }
            if(i + 1 == windowY + heroRealm.length){
                heroRealm[j] = realWorld[i].substring(windowX, windowX + heroRealm[j].length() - 4) + "\n```";
            }else{
                heroRealm[j] = realWorld[i].substring(windowX, windowX + heroRealm[j].length() - 1) + "\n";
            }
            j++;
        }
        heroRealm[3] = changeChar(heroRealm[3], heroRealm[3].length() / 2 - 1, '@');
    }

    public static String changeChar(String str, int ind, char ch){
        char[] cha = new char[str.length()];
        for (int i = 0; i < str.length(); i++) {
            cha[i] = str.charAt(i);
        }
        cha[ind] = ch;
        StringBuilder ebal = new StringBuilder();
        int test = 0;
        for(char r: cha){
            if(r == 'w'){
                test++;
            }
            ebal.append(r);
        }
        return String.valueOf(ebal);
    }

