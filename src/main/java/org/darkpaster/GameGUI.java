    public static void checkTarget(){
        boolean test = false;
        for(Mob mob: mobs){
            if(Coordinates.pointDistanceXY(hero, mob) < 4){
                target = mob;
                test = true;
            }
        }
        if(!test){
            target = null;
        }
    }


    public static void enemyTurn(){
        for(Mob m: mobs){
            if(Coordinates.pointDistanceXY(hero, m) < 2){
                m.attack(hero);
                return;
            }
            if(m.isChasing){
                m.chase(hero);
                return;
            }
            m.move();
        }
    }

